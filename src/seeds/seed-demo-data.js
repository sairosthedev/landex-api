export function mapListingToDocument(listing, sellerId) {
  return {
    listingReference: listing.reference,
    sellerId,
    listingType: 'SALE',
    status: 'ACTIVE',
    title: listing.title,
    description: listing.description,
    askingPrice: listing.price,
    currency: 'USD',
    priceNegotiable: false,
    propertyType: USE_TO_PROPERTY[listing.use] || 'OTHER',
    tenureType: TITLE_TO_TENURE[listing.titleType] || 'OTHER',
    areaSqm: Math.round(listing.sizeHa * 10000 * 100) / 100,
    province: listing.province,
    district: listing.district,
    address: listing.address,
    latitude: listing.latitude,
    longitude: listing.longitude,
    location: {
      type: 'Point',
      coordinates: [listing.longitude, listing.latitude],
    },
    featured: listing.featured,
    verified: listing.verified,
    viewCount: 0,
    publishedAt: new Date(),
    deletedAt: null,
  };
}
